//! Core module types for the LuperIQ CMS platform.
//!
//! Contains the `CmsModule` trait, `AdminView`, `ModuleMeta`, `ModulePricing`,
//! and the static `KNOWN_MODULES` manifest. These types are shared across all
//! module crates via the `luperiq-module-api` crate.

use crate::context::AppContext;
use axum::Router;

/// Trait implemented by every CMS module.
pub trait CmsModule: Send + Sync {
    /// Unique slug (e.g. "smtp", "forms").
    fn slug(&self) -> &str;

    /// Human-readable name.
    fn name(&self) -> &str;

    /// Version string.
    fn version(&self) -> &str;

    /// Short description of the module.
    fn description(&self) -> &str {
        ""
    }

    /// Category for grouping (e.g. "Communication", "Content", "Design").
    fn category(&self) -> &str {
        "General"
    }

    /// Module slugs this module depends on.
    fn dependencies(&self) -> &[&str] {
        &[]
    }

    /// Axum routes to merge into the main app.
    /// Called once during app assembly in main.rs.
    fn routes(&self, ctx: &AppContext) -> Option<Router> {
        let _ = ctx;
        None
    }

    /// Admin sidebar entries this module contributes.
    fn admin_views(&self) -> Vec<AdminView> {
        vec![]
    }

    /// JavaScript snippet injected into the admin panel.
    /// Should define handler functions referenced by admin_views.
    fn admin_js(&self) -> Option<String> {
        None
    }

    /// CSS snippet injected into the admin panel.
    fn admin_css(&self) -> Option<String> {
        None
    }

    /// Module slugs whose JS must be loaded before this module's JS.
    fn admin_js_deps(&self) -> Vec<&str> {
        vec![]
    }
}

/// Metadata for an admin sidebar entry added by a module.
#[derive(Debug, Clone)]
pub struct AdminView {
    /// Internal ID used as `data-view` attribute (e.g. "smtp-settings").
    pub id: String,
    /// Human-readable label in the sidebar.
    pub label: String,
    /// Section header (e.g. "Modules"). Views with the same section are grouped.
    pub section: String,
}

/// Pricing info for a module (all values in US cents).
#[derive(Debug, Clone)]
pub struct ModulePricing {
    /// Monthly price in cents. 0 = included in subscription tier.
    pub monthly: u32,
    /// Yearly price in cents. 0 = included in subscription tier.
    pub yearly: u32,
    /// Lifetime price in cents. 0 = included in subscription tier.
    pub lifetime: u32,
}

/// Rich metadata for a single CMS module.
#[derive(Debug, Clone)]
pub struct ModuleMeta {
    /// URL-safe slug (e.g. "smtp", "pest-control").
    pub slug: &'static str,
    /// Human-readable name.
    pub name: &'static str,
    /// SemVer version string.
    pub version: &'static str,
    /// Category for grouping (e.g. "Communication", "Operations").
    pub category: &'static str,
    /// One-line description.
    pub description: &'static str,
    /// If true, only visible on central/standalone instances.
    pub central_only: bool,
    /// 2-3 sentence description for module detail cards.
    pub long_description: &'static str,
    /// Bullet-point feature list (4-6 items).
    pub features: &'static [&'static str],
    /// Admin sidebar section this module appears in.
    pub admin_section: &'static str,
    /// Human-readable dependency names (display only).
    pub dependencies_display: &'static [&'static str],
    /// Minimum subscription tier: "free" or "pro". All modules are free with LuperIQ branding.
    pub tier_required: &'static str,
    /// Pricing (0/0/0 = included in tier).
    pub pricing: ModulePricing,
    /// Number of trial days. 0 = no trial available.
    pub trial_days: u8,
}

/// Helper: find a module by slug.
pub fn find_module_meta(slug: &str) -> Option<&'static ModuleMeta> {
    KNOWN_MODULES.iter().find(|m| m.slug == slug)
}

