//! Industry sub-specialty taxonomy — data-only layer on top of SiteTypeDefinition.
//!
//! Categories group industries for funnel/catalog display.
//! Sub-specialties define niche variants within an industry that inherit modules,
//! theme, and nav from their parent but override display text, CTA, and pages.

use serde::{Deserialize, Serialize};

// ─── Data Structures ────────────────────────────────────────────────────────

/// Top-level grouping for the site catalog funnel.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct IndustryCategory {
    pub slug: String,
    pub name: String,
    pub emoji: String,
    pub description: String,
    pub display_order: u32,
}

/// A niche within a parent industry. Inherits modules/theme from the parent
/// SiteTypeDefinition but can override display, CTA, nav, pages, and SEO.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct IndustrySubSpecialty {
    pub slug: String,
    /// Links to an existing SiteTypeDefinition slug (e.g. "restaurant", "hvac")
    pub parent_industry: String,
    /// Links to an IndustryCategory slug (e.g. "restaurants", "home-services")
    pub category: String,
    pub name: String,
    pub emoji: String,
    pub description: String,
    pub tagline: String,
    /// Used for SEO title templates: "{business_name} -- {seo_title_suffix} in {city}"
    pub seo_title_suffix: String,
    pub default_cta_text: String,
    pub default_cta_url: String,
    /// Additional nav items beyond what the parent industry provides
    pub unique_nav_items: Vec<String>,
    /// Additional pages to create beyond the parent's defaults
    pub unique_pages: Vec<String>,
    pub display_order: u32,
}

