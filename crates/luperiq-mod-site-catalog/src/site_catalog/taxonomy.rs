// ─── Lookup Helpers ─────────────────────────────────────────────────────────

/// Find all sub-specialties belonging to a given category slug.
pub fn subspecialties_by_category(category: &str) -> Vec<IndustrySubSpecialty> {
    all_subspecialties()
        .into_iter()
        .filter(|s| s.category == category)
        .collect()
}

/// Find all sub-specialties for a given parent industry slug.
pub fn subspecialties_by_industry(parent: &str) -> Vec<IndustrySubSpecialty> {
    all_subspecialties()
        .into_iter()
        .filter(|s| s.parent_industry == parent)
        .collect()
}

/// Look up a single sub-specialty by its slug.
pub fn find_subspecialty(slug: &str) -> Option<IndustrySubSpecialty> {
    all_subspecialties().into_iter().find(|s| s.slug == slug)
}

/// Look up a category by slug.
pub fn find_category(slug: &str) -> Option<IndustryCategory> {
    all_categories().into_iter().find(|c| c.slug == slug)
}

// ─── Constructor Helpers (private) ──────────────────────────────────────────

fn cat(slug: &str, name: &str, emoji: &str, desc: &str, order: u32) -> IndustryCategory {
    IndustryCategory {
        slug: slug.into(),
        name: name.into(),
        emoji: emoji.into(),
        description: desc.into(),
        display_order: order,
    }
}

fn sub(
    slug: &str,
    parent: &str,
    category: &str,
    name: &str,
    emoji: &str,
    desc: &str,
    tagline: &str,
    cta_text: &str,
    cta_url: &str,
    order: u32,
) -> IndustrySubSpecialty {
    IndustrySubSpecialty {
        slug: slug.into(),
        parent_industry: parent.into(),
        category: category.into(),
        name: name.into(),
        emoji: emoji.into(),
        description: desc.into(),
        tagline: tagline.into(),
        seo_title_suffix: name.into(),
        default_cta_text: cta_text.into(),
        default_cta_url: cta_url.into(),
        unique_nav_items: vec![],
        unique_pages: vec![],
        display_order: order,
    }
}

// ─── Tests ──────────────────────────────────────────────────────────────────

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn categories_have_unique_slugs() {
        let cats = all_categories();
        let mut slugs: Vec<&str> = cats.iter().map(|c| c.slug.as_str()).collect();
        let len = slugs.len();
        slugs.sort();
        slugs.dedup();
        assert_eq!(slugs.len(), len, "duplicate category slugs found");
    }

    #[test]
    fn subspecialties_have_unique_slugs() {
        let subs = all_subspecialties();
        let mut slugs: Vec<&str> = subs.iter().map(|s| s.slug.as_str()).collect();
        let len = slugs.len();
        slugs.sort();
        slugs.dedup();
        assert_eq!(slugs.len(), len, "duplicate sub-specialty slugs found");
    }

    #[test]
    fn all_subspecialties_reference_valid_categories() {
        let cats = all_categories();
        let cat_slugs: Vec<&str> = cats.iter().map(|c| c.slug.as_str()).collect();
        for sub in all_subspecialties() {
            assert!(
                cat_slugs.contains(&sub.category.as_str()),
                "sub-specialty '{}' references unknown category '{}'",
                sub.slug,
                sub.category
            );
        }
    }

    #[test]
    fn subspecialty_count_is_at_least_90() {
        let count = all_subspecialties().len();
        assert!(
            count >= 90,
            "expected at least 90 sub-specialties, got {count}"
        );
    }

    #[test]
    fn category_count_is_at_least_18() {
        let count = all_categories().len();
        assert!(count >= 18, "expected at least 18 categories, got {count}");
    }

    #[test]
    fn lookup_by_category_works() {
        let restaurant_subs = subspecialties_by_category("restaurants");
        assert!(
            restaurant_subs.len() >= 5,
            "expected at least 5 restaurant sub-specialties"
        );
        for s in &restaurant_subs {
            assert_eq!(s.category, "restaurants");
        }
    }

    #[test]
    fn lookup_by_industry_works() {
        let hvac_subs = subspecialties_by_industry("hvac");
        assert!(
            hvac_subs.len() >= 3,
            "expected at least 3 HVAC sub-specialties"
        );
        for s in &hvac_subs {
            assert_eq!(s.parent_industry, "hvac");
        }
    }

    #[test]
    fn find_subspecialty_works() {
        let result = find_subspecialty("food-truck");
        assert!(result.is_some());
        let ft = result.unwrap();
        assert_eq!(ft.name, "Food Truck");
        assert_eq!(ft.parent_industry, "restaurant");
    }

    #[test]
    fn find_category_works() {
        let result = find_category("auto");
        assert!(result.is_some());
        assert_eq!(result.unwrap().name, "Auto Services");
    }
}
